static bool ts_lex(TSLexer *lexer, TSStateId state) {
  START_LEXER();
  eof = lexer->eof(lexer);
  switch (state) {
    case 0:
      if (eof) ADVANCE(25);
      if (lookahead == '\t') ADVANCE(28);
      if (lookahead == '\n') ADVANCE(29);
      if (lookahead == '\r') ADVANCE(1);
      if (lookahead == ' ') ADVANCE(31);
      if (lookahead == '#') ADVANCE(2);
      if (lookahead == '+') ADVANCE(47);
      if (lookahead == ',') ADVANCE(37);
      if (lookahead == '-') ADVANCE(48);
      if (lookahead == '0') ADVANCE(45);
      if (lookahead == 'I') ADVANCE(13);
      if (lookahead == 'N') ADVANCE(6);
      if (lookahead == '[') ADVANCE(36);
      if (lookahead == ']') ADVANCE(38);
      if (lookahead == '{') ADVANCE(3);
      if (('1' <= lookahead && lookahead <= '9')) ADVANCE(46);
      END_STATE();
    case 1:
      if (lookahead == '\n') ADVANCE(30);
      END_STATE();
    case 2:
      if (lookahead == '!') ADVANCE(32);
      END_STATE();
    case 3:
      if (lookahead == '-') ADVANCE(34);
      END_STATE();
    case 4:
      if (lookahead == '.') ADVANCE(22);
      if (lookahead == 'e') ADVANCE(21);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(4);
      END_STATE();
    case 5:
      if (lookahead == 'N') ADVANCE(41);
      END_STATE();
    case 6:
      if (lookahead == 'a') ADVANCE(5);
      END_STATE();
    case 7:
      if (lookahead == 'f') ADVANCE(10);
      END_STATE();
    case 8:
      if (lookahead == 'f') ADVANCE(12);
      END_STATE();
    case 9:
      if (lookahead == 'i') ADVANCE(17);
      END_STATE();
    case 10:
      if (lookahead == 'i') ADVANCE(14);
      END_STATE();
    case 11:
      if (lookahead == 'i') ADVANCE(18);
      END_STATE();
    case 12:
      if (lookahead == 'i') ADVANCE(15);
      END_STATE();
    case 13:
      if (lookahead == 'n') ADVANCE(7);
      END_STATE();
    case 14:
      if (lookahead == 'n') ADVANCE(9);
      END_STATE();
    case 15:
      if (lookahead == 'n') ADVANCE(11);
      END_STATE();
    case 16:
      if (lookahead == 'n') ADVANCE(8);
      END_STATE();
    case 17:
      if (lookahead == 't') ADVANCE(19);
      END_STATE();
    case 18:
      if (lookahead == 't') ADVANCE(20);
      END_STATE();
    case 19:
      if (lookahead == 'y') ADVANCE(40);
      END_STATE();
    case 20:
      if (lookahead == 'y') ADVANCE(39);
      END_STATE();
    case 21:
      if (lookahead == '+' ||
          lookahead == '-') ADVANCE(23);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(43);
      END_STATE();
    case 22:
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(42);
      END_STATE();
    case 23:
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(43);
      END_STATE();
    case 24:
      if (('0' <= lookahead && lookahead <= '9') ||
          ('A' <= lookahead && lookahead <= 'F')) ADVANCE(44);
      END_STATE();
    case 25:
      ACCEPT_TOKEN(ts_builtin_sym_end);
      END_STATE();
    case 26:
      ACCEPT_TOKEN(sym_free_text);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(26);
      END_STATE();
    case 27:
      ACCEPT_TOKEN(sym_free_text);
      if (eof) ADVANCE(25);
      if (lookahead != 0 &&
          lookahead != '\n') ADVANCE(26);
      END_STATE();
    case 28:
      ACCEPT_TOKEN(sym__tab);
      END_STATE();
    case 29:
      ACCEPT_TOKEN(anon_sym_LF);
      END_STATE();
    case 30:
      ACCEPT_TOKEN(anon_sym_CR_LF);
      END_STATE();
    case 31:
      ACCEPT_TOKEN(anon_sym_);
      END_STATE();
    case 32:
      ACCEPT_TOKEN(anon_sym_POUND_BANG);
      END_STATE();
    case 33:
      ACCEPT_TOKEN(sym_line_comment_prefix);
      END_STATE();
    case 34:
      ACCEPT_TOKEN(sym_block_comment_open);
      END_STATE();
    case 35:
      ACCEPT_TOKEN(sym_block_comment_close);
      END_STATE();
    case 36:
      ACCEPT_TOKEN(anon_sym_LBRACK);
      END_STATE();
    case 37:
      ACCEPT_TOKEN(anon_sym_COMMA);
      END_STATE();
    case 38:
      ACCEPT_TOKEN(anon_sym_RBRACK);
      END_STATE();
    case 39:
      ACCEPT_TOKEN(anon_sym_DASHInfinity);
      END_STATE();
    case 40:
      ACCEPT_TOKEN(anon_sym_Infinity);
      END_STATE();
    case 41:
      ACCEPT_TOKEN(anon_sym_NaN);
      END_STATE();
    case 42:
      ACCEPT_TOKEN(aux_sym_double_literal_token1);
      if (lookahead == 'e') ADVANCE(21);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(42);
      END_STATE();
    case 43:
      ACCEPT_TOKEN(aux_sym_double_literal_token1);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(43);
      END_STATE();
    case 44:
      ACCEPT_TOKEN(aux_sym_natural_literal_token1);
      if (('0' <= lookahead && lookahead <= '9') ||
          ('A' <= lookahead && lookahead <= 'F')) ADVANCE(44);
      END_STATE();
    case 45:
      ACCEPT_TOKEN(aux_sym_natural_literal_token2);
      if (lookahead == '.') ADVANCE(22);
      if (lookahead == 'e') ADVANCE(21);
      if (lookahead == 'x') ADVANCE(24);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(4);
      END_STATE();
    case 46:
      ACCEPT_TOKEN(aux_sym_natural_literal_token2);
      if (lookahead == '.') ADVANCE(22);
      if (lookahead == 'e') ADVANCE(21);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(46);
      END_STATE();
    case 47:
      ACCEPT_TOKEN(anon_sym_PLUS);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(4);
      END_STATE();
    case 48:
      ACCEPT_TOKEN(anon_sym_DASH);
      if (lookahead == '-') ADVANCE(33);
      if (lookahead == 'I') ADVANCE(16);
      if (lookahead == '}') ADVANCE(35);
      if (('0' <= lookahead && lookahead <= '9')) ADVANCE(4);
      END_STATE();
    default:
      return false;
  }
}


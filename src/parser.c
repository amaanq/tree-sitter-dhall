static inline bool aux_sym_double_quote_escaped_token1_character_set_1(int32_t c) {
  return (c < 'b'
    ? (c < '/'
      ? (c < '$'
        ? c == '"'
        : c <= '$')
      : (c <= '/' || c == '\\'))
    : (c <= 'b' || (c < 'r'
      ? (c < 'n'
        ? c == 'f'
        : c <= 'n')
      : (c <= 'r' || c == 't'))));
}

